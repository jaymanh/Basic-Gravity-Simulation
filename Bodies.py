class body:

    def __init__(self, name, mas, pos, vel):
        self.name = name
        self.mas = mas
        self.pos = pos
        self.vel = vel

    def name(self):
        return self.name

    def mas(self):
        return self.mas

    def pos(self):
        return self.pos

    def vel(self):
        return self.vel
    

