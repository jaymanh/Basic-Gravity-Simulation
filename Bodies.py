class body:

    def __init__(self, name, mas, pos, vel, color, radius):
        self.name = name
        self.mas = mas
        self.pos = pos
        self.vel = vel
        self.color = color
        self.radius = radius

    def name(self):
        return self.name

    def mas(self):
        return self.mas

    def pos(self):
        return self.pos

    def vel(self):
        return self.vel
    
    def color(self):
        return self.color
    
    def radius(self):
        return self.radius